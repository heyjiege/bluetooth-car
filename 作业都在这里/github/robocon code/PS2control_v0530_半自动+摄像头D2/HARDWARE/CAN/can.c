unsigned int CAN_Time_Out = 0;

static void CAN_Delay_Us(unsigned int t)
{
	int i;
	for(i=0;i<t;i++)
	{
		int a=9;
		while(a--);
	}
}

void CAN1_Configuration(void)
{
	CAN_InitTypeDef        can;
	CAN_FilterInitTypeDef  can_filter;
	GPIO_InitTypeDef 	   gpio;
	NVIC_InitTypeDef   	   nvic;

	RCC_APB1PeriphClockCmd(RCC_APB1Periph_CAN1,  ENABLE);
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA, ENABLE);
    
	gpio.GPIO_Pin = GPIO_Pin_11;
	gpio.GPIO_Mode = GPIO_Mode_IPU;
    gpio.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOA, &gpio);	//CAN_RX
	
	gpio.GPIO_Pin = GPIO_Pin_12;	   
    gpio.GPIO_Mode = GPIO_Mode_AF_PP;
	gpio.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOA, &gpio);    //CAN_TX

    nvic.NVIC_IRQChannel = USB_HP_CAN1_TX_IRQn;
    nvic.NVIC_IRQChannelPreemptionPriority = 0;
    nvic.NVIC_IRQChannelSubPriority = 1;
    nvic.NVIC_IRQChannelCmd = ENABLE;
    NVIC_Init(&nvic);
    
    nvic.NVIC_IRQChannel = USB_LP_CAN1_RX0_IRQn;  
    nvic.NVIC_IRQChannelPreemptionPriority = 1;
    nvic.NVIC_IRQChannelSubPriority = 0;
    nvic.NVIC_IRQChannelCmd = ENABLE;
    NVIC_Init(&nvic);

	CAN_DeInit(CAN1);   //初始化CAN1

	can.CAN_TTCM = DISABLE; //CAN_TTCM 用来使能或者失能 时间触发通讯模式
	can.CAN_ABOM = DISABLE; //CAN_ABOM 用来使能或者失能 自动离线管理
	can.CAN_AWUM = DISABLE; //CAN_AWUM 用来使能或者失能 自动唤醒模式
	can.CAN_NART = DISABLE;	//CAN_NARM 用来使能或者失能 非自动重传输模式  
	can.CAN_RFLM = DISABLE;	//CAN_RFLM 用来使能或者失能 接收 FIFO 锁定模式															
	can.CAN_TXFP = ENABLE;	//CAN_TXFP 用来使能或者失能 发送 FIFO 优先级	
 	can.CAN_Mode = CAN_Mode_Normal; //CAN 硬件工作在正常模式
	can.CAN_SJW = CAN_SJW_1tq;   //重新同步跳跃宽度 1 个时间单位
	can.CAN_BS1 = CAN_BS1_5tq;   //时间段 1 为 5 个时间单位
	can.CAN_BS2 = CAN_BS2_3tq;   //时间段 2 为 3 个时间单位
	can.CAN_Prescaler = 4;     //CAN BaudRate 36/(1+5+3)/4=1Mbps    //CAN_Prescaler 设定了一个时间单位的长度，它的范围是 1 到 1024
	CAN_Init(CAN1, &can);
    //以下是CAN过滤器的设置
	can_filter.CAN_FilterNumber = 0;   //CAN_FilterNumber 指定了待初始化的过滤器，它的范围是 1 到 13
	can_filter.CAN_FilterMode = CAN_FilterMode_IdMask; //标识符屏蔽位模式      //CAN_FilterMode_IdList  标识符列表模式
	can_filter.CAN_FilterScale = CAN_FilterScale_32bit;  //1 个 32 位过滤器
	can_filter.CAN_FilterIdHigh = 0x0000;   //CAN_FilterIdHigh 用来设定过滤器标识符
	can_filter.CAN_FilterIdLow = 0x0000;    //CAN_FilterIdHigh 用来设定过滤器标识符
	can_filter.CAN_FilterMaskIdHigh = 0x0000;//CAN_FilterMaskIdHigh 用来设定过滤器屏蔽标识符或者过滤器标识符
	can_filter.CAN_FilterMaskIdLow = 0x0000; //CAN_FilterMaskIdLow 用来设定过滤器屏蔽标识符或者过滤器标识符
	can_filter.CAN_FilterFIFOAssignment = 0;  //CAN_FilterFIFO 设定了指向过滤器的 FIFO
	can_filter.CAN_FilterActivation = ENABLE; //CAN_FilterActivation 使能或者失能过滤器
	CAN_FilterInit(&can_filter);
    //函数CAN_ITConfig: 使能或者失能指定的 CAN 中断
    CAN_ITConfig(CAN1,CAN_IT_FMP0,ENABLE);  //FIFO0 消息挂号中断屏蔽
    CAN_ITConfig(CAN1,CAN_IT_TME,ENABLE);   //发送邮箱空中断屏蔽
}

unsigned char can_tx_success_flag=0;
void USB_HP_CAN1_TX_IRQHandler(void) //CAN TX
{
    if (CAN_GetITStatus(CAN1,CAN_IT_TME)!= RESET)
	{
	   CAN_ClearITPendingBit(CAN1,CAN_IT_TME);
       can_tx_success_flag=1;
    }
}

/****************************************************************************************
                                       复位指令
Group   取值范围 0-7
Number  取值范围 0-15，其中Number==0时，为广播发送
*****************************************************************************************/
void CAN_RoboModule_DRV_Reset(unsigned char Group,unsigned char Number)
{
    unsigned short can_id = 0x000;
    CanTxMsg tx_message;
    
    tx_message.IDE = CAN_ID_STD;    //标准帧    //IDE 用来设定消息标识符的类型（2种）
    tx_message.RTR = CAN_RTR_DATA;  //数据帧    //RTR 用来设定待传输消息的帧类型。它可以设置为数据帧或者远程帧。
    tx_message.DLC = 0x08;          //帧长度为8 //DLC 用来设定待传输消息的帧长度（它的取值范围是 0 到 0x8）
    
    if((Group<=7)&&(Number<=15))
    {
        can_id |= Group<<8;
        can_id |= Number<<4;
    }
    else
    {
        return;
    }
    
    tx_message.StdId = can_id;      //帧ID为传入参数的CAN_ID   //StdId 用来设定标准标识符
                                   //Data[8]包含了待传输数据，它的取值范围为 0 到 0xFF。
    tx_message.Data[0] = 0x55;
    tx_message.Data[1] = 0x55;
    tx_message.Data[2] = 0x55;
    tx_message.Data[3] = 0x55;
    tx_message.Data[4] = 0x55;
    tx_message.Data[5] = 0x55;
    tx_message.Data[6] = 0x55;
    tx_message.Data[7] = 0x55;
    
    can_tx_success_flag = 0;
    CAN_Transmit(CAN1,&tx_message);   //函数CAN_Transmit: 开始一个消息的传输
    
    CAN_Time_Out = 0;
    while(can_tx_success_flag == 0)
    {
        CAN_Delay_Us(1);
        CAN_Time_Out++;
        if(CAN_Time_Out>100)
        {
            break;
        }
    }
}

